package datastructure;

public class HashNode<Integer, String> {
    int key;
    String value;
    HashNode<Integer, String> next;

    public HashNode()
    {
        this.key = key;
        this.value = value;
    }

    public int getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
